//-----------------------------------------------------------------------------
// Bridge
//-----------------------------------------------------------------------------

function syscall(cmd, ...args) {
  return window.ipc.callMain("host", [cmd, ...args]);
}

//-----------------------------------------------------------------------------
// System dialogs
//-----------------------------------------------------------------------------

export function appQuit(options) {
  return syscall("quit", options);
}

export function appLog(message) {
  return syscall("log", message);
}
