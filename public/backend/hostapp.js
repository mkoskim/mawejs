//*****************************************************************************
//*****************************************************************************
//
// Access to Electron host system dialogs
//
//*****************************************************************************
//*****************************************************************************

module.exports = {
  info,
  quit,
  log,
  beep,
}

//-----------------------------------------------------------------------------

const {app, shell} = require("electron");

//-----------------------------------------------------------------------------

function info() {
  return {
    name: app.getName(),
    version: app.getVersion(),
  }
}

function quit() {
  app.quit();
}

function log(message) {
  console.log(message)
}

function beep() {
  console.log("Beep")
  shell.beep()
}
